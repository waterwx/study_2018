# coding = utf-8
classmates = ['Michael','bob','Tracy']
print("初始的list")
print(classmates)
print("---------------------")

# 添加元素
classmates.append('Adam')
print("append---添加元素")
print(classmates)
print("---------------------")

# 插入元素
classmates.insert(1,'yaoming')
print("insert---插入元素")
print(classmates)
print("---------------------")

# 删除元素
classmates.pop(2)
print("pop---删除元素")
print(classmates)
print("---------------------")

# 替换元素
classmates[2] = 'liuxiang'
print("替换元素")
print(classmates)
print("---------------------")
